public class Dog {
    private String name;
    private String color;
    private Double height;

    public Dog(String name, String color, Double height) {
        this.name = name;
        this.color = color;
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

    public Double getHeight() {
        return height;
    }

  
   
    
    
}
