    public void details() {

        name = txtName.getText();

        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "You don't write your name.");
            txtName.requestFocus();
        }

        if (rdMale.isSelected()) {
            gend = "Male";
        } else if (rdFemale.isSelected()) {
            gend = "Female";
        } else {
            JOptionPane.showMessageDialog(rootPane, "You don't select gender.");
        }

        age = comAge.getSelectedItem().toString();
        if (comAge.getSelectedItem().toString().equals("5")) {
            JOptionPane.showMessageDialog(rootPane, "Please input your age.");
        }

        for (int i = 0; i < chkPassed.getComponentCount(); i++) {
            JCheckBox cb = (JCheckBox) chkPassed.getComponent(i);
            if (cb.isSelected()) {
                pass += cb.getText() + " ";
            }
        }
        if (pass.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Input your qualification");
        }

        country = comCountry.getSelectedItem().toString();
        if (comCountry.getSelectedItem().toString().equals("--Country--")) {
            JOptionPane.showMessageDialog(rootPane, "Please enter your country.");
        }
    }

